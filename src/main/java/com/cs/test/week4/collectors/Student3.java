package com.cs.test.week4.collectors;

public class Student3 {
	private String name;
	private int age;
	private String className;

	public Student3(String name,String className,int age){
        this.name=name;
        this.age=age;
        this.className = className;
    }
	public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public String getClassName() {
		return className;
	}
}
